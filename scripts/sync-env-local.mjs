import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const envPath = resolve(root, '.env.local');
const outputPath = resolve(root, 'src/environments/environment.local.ts');

const fallback = {
  supabaseUrl: 'http://127.0.0.1:54321',
  supabaseAnonKey: 'prototype-anon-key',
  siteUrl: 'http://localhost:4200',
  siteUrlPublic: 'http://localhost:4200',
};

function parseEnvFile(path) {
  if (!existsSync(path)) return {};

  return Object.fromEntries(
    readFileSync(path, 'utf8')
      .split(/\r?\n/)
      .map((line) => line.trim())
      .filter((line) => line && !line.startsWith('#') && line.includes('='))
      .map((line) => {
        const separatorIndex = line.indexOf('=');
        const key = line.slice(0, separatorIndex).trim();
        const rawValue = line.slice(separatorIndex + 1).trim();
        const value = rawValue.replace(/^['"]|['"]$/g, '');
        return [key, value];
      }),
  );
}

function pick(env, keys, fallbackValue) {
  for (const key of keys) {
    if (env[key]) return env[key];
  }
  return fallbackValue;
}

const env = parseEnvFile(envPath);
const environment = {
  production: false,
  supabaseUrl: pick(
    env,
    ['SUPABASE_URL', 'NEXT_PUBLIC_SUPABASE_URL'],
    fallback.supabaseUrl,
  ),
  supabaseAnonKey: pick(
    env,
    ['SUPABASE_ANON_KEY', 'NEXT_PUBLIC_SUPABASE_ANON_KEY'],
    fallback.supabaseAnonKey,
  ),
  siteUrl: pick(env, ['SITE_URL', 'NEXT_PUBLIC_SITE_URL'], fallback.siteUrl),
  siteUrlPublic: pick(
    env,
    ['SITE_URL_PUBLIC', 'NEXT_PUBLIC_SITE_URL_PUBLIC'],
    fallback.siteUrlPublic,
  ),
};

const contents = `// Generated by scripts/sync-env-local.mjs from .env.local.
// Do not commit real local values.
export const environment = ${JSON.stringify(environment, null, 2)};
`;

writeFileSync(outputPath, contents);
console.log('Generated src/environments/environment.local.ts from .env.local');
